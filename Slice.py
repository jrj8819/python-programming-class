# 문자열 슬라이싱
"""
문자열의 인덱스를 활용하여
문자열의 일부를 추려내는 방법

문자열변수[슬라이스를 시작하는 인덱스번호 : 끝나기 이후
인덱스번호]
"""

han = "가나다라마바사"
print(han[2:5])

"""
인덱스가 왼쪽 부터 시작하면(0이면) 생략할 수 있다.
"""
print(han[0:3])
print(han[:3])

"""
끝나는 인덱스 번호를 생략하면 문자열의 마지막 글자까지
슬라이싱한다.
"""
print(han[4:])

